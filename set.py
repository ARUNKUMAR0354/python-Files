# Create the set in python

set1 = set()
print("initial set using empty: ")
print(set1)

set1 = set("arunkumar")
print("\nset with use the string:")
print(set1)

set1 = set(["arun", "kumar", "krish"])
print("\nset with use the list: ")
print(set1)

set1 = set(["arun", 1, 2, "kumar", 3, "krish"])
print("set with use tha mixed values:")
print(set1)
